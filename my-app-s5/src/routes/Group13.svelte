<script>
	let m = $state({ x: 0, y: 0 });

	// @ts-ignore
	function onpointermove(event) {
		m.x = event.clientX;
		m.y = event.clientY;
	}
</script>

<div {onpointermove}>
	The pointer is at {Math.round(m.x)} x {Math.round(m.y)}
</div>

<style>
	div {
		position: fixed;
		left: 50px;
		top: 50px;
		width: 100%;
		height: 100%;
		padding: 1rem;
	}
</style>
